def get_tutorial_question(seen_ids: set = None) -> dict | None:
    """
    Return a random tutorial question not in seen_ids.

    Args:
        seen_ids: Set of question IDs already shown to the player.
                 If None, treated as an empty set.

    Returns:
        A dictionary representing a tutorial question, or None if no questions available.
        Returns a copy to prevent modification of the original question data.
    """
    import random

    if seen_ids is None:
        seen_ids = set()

    available = [q for q in TUTORIAL_QUESTIONS if q['id'] not in seen_ids]

    if not available:
        # Wrap around if all questions have been seen
        available = TUTORIAL_QUESTIONS

    return dict(random.choice(available)) if available else None
